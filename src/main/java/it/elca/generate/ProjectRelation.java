package it.elca.generate;

/**
 * Project Relation
 */
public class ProjectRelation {
	private String type;
    private String sxTable;
    private String sxName;
    private String sxSelect;
    private String dxTable;
    private String dxName;
    private String dxSelect;
    
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getSxTable() {
		return sxTable;
	}
	public void setSxTable(String sxTable) {
		this.sxTable = sxTable;
	}
	public String getSxName() {
		return sxName;
	}
	public void setSxName(String sxName) {
		this.sxName = sxName;
	}
	public String getSxSelect() {
		return sxSelect;
	}
	public void setSxSelect(String sxSelect) {
		this.sxSelect = sxSelect;
	}
	public String getDxTable() {
		return dxTable;
	}
	public void setDxTable(String dxTable) {
		this.dxTable = dxTable;
	}
	public String getDxName() {
		return dxName;
	}
	public void setDxName(String dxName) {
		this.dxName = dxName;
	}
	public String getDxSelect() {
		return dxSelect;
	}
	public void setDxSelect(String dxSelect) {
		this.dxSelect = dxSelect;
	}
    
    
}
